#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t		i;
	int			sign;
	long		result;

	i = 0;
	sign = 1;
	result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = sign * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if ((result * 10 + (str[i] - '0')) < result)
			return (-(sign == 1));
		result = str[i] - '0' + result * 10;
		i++;
	}
	return ((int)(result * sign));
}
